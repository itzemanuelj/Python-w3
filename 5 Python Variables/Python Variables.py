# Python Variables

# Creating Variables
name = "Justin"

age = 28

print(name)
print(age)

# Casting
lname = str("Jones")
age = int(28)
age = float(28)

print(lname)
print(age)
print(age)

# Get the Type
print(type(age))
print(type(name))

# Single or Double Quotes?
name = 'Justin'
lname = "Jones"

# Case-Sensitive
a = True
A = False

