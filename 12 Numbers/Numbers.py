## floats
#Float can also be scientific numbers with an "e" to indicate the power of 10.

x = 1e2
y = 12e2
z = -86e12

print(type(x))
print(type(y))
print(type(z))

## complex
# Complex numbers are written with a "j" as the imaginary part:

x = 1 + 5j
y = 5j
z = 12 * 2j

print(type(x))
print(type(y))
print(type(z))


## type conversion

x = int(x)
y = float(y)
z = complex(z)

print(type(x))
print(type(y))
print(type(z))

